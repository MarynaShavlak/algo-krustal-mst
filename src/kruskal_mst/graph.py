"""Приклад зваженого графа та фіксовані розкладки координат для схем.

``build_graph`` повертає прикладовий зважений граф:
7 вершин (A..G) і 11 зважених ребер. Фіксовані координати ``POS`` тримають
вигляд графа однаковим на всіх схемах. Окремі розкладки потрібні для двох
схем-ілюстрацій: дерева вказівників DSU (``POS_DSU``) та доведення коректності
(``POS_CUT``, ``POS_EX``).
"""

from __future__ import annotations

import networkx as nx

#: Ребра прикладового графа у вигляді (u, v, вага).
EDGES = [
    ("A", "B", 7), ("A", "D", 5), ("B", "C", 8), ("B", "D", 9),
    ("B", "E", 7), ("C", "E", 5), ("D", "E", 15), ("D", "F", 6),
    ("E", "F", 8), ("E", "G", 9), ("F", "G", 11),
]


def build_graph() -> nx.Graph:
    """Побудувати прикладовий зважений неорієнтований граф."""
    G = nx.Graph()
    for u, v, w in EDGES:
        G.add_edge(u, v, weight=w)
    return G


